#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			cnt;
	unsigned char	*str;

	cnt = 0;
	str = (unsigned char *)s;
	while (cnt < n)
	{
		if ((unsigned char)c == str[cnt])
			return ((unsigned char *)s + cnt);
		cnt++;
	}
	return (NULL);
}
