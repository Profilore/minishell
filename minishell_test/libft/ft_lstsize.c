#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		cnt;
	t_list	*tmp;

	if (!lst)
		return (0);
	cnt = 0;
	tmp = lst;
	while (tmp)
	{
		cnt++;
		tmp = tmp->next;
	}
	return (cnt);
}
