#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		cnt;
	int		len1;
	int		len2;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen((char *)s1);
	len2 = ft_strlen((char *)s2);
	str = (char *)malloc(sizeof(char) * (len1 + len2 + 1));
	if (!str)
		return (NULL);
	cnt = -1;
	while (s1[++cnt])
		str[cnt] = s1[cnt];
	cnt = -1;
	while (s2[++cnt])
		str[len1++] = s2[cnt];
	str[len1] = '\0';
	return (str);
}
